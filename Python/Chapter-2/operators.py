# arithmatic operators
a=1
b=4
c=a+b
print(c)
# assignment operators
a=4-2 #assign 4-2 in a
b+=3 #add 3 in b everytime
c*=2 #multiply c by 2 everytime
# comparison operators
print(a==b) #check if a is equal to b
print(a!=b) #check if a is not equal to b
print(a>b) #check if a is greater than b
print(a<b) #check if a is less than b
print(a>=b) #check if a is greater than or equal to b
print(a<=b) #check if a is less than or equal to b
# logical operators
print(a>b and a<b) #check if a is greater than b and a is less than b
print(a>b or a<b) #check if a is greater than b or a is less than b
print(not(a>b and a<b)) #check if a is not greater than b and a is less than b
# identity operators
print(a is b) #check if a is b
print(a is not b) #check if a is not b
# membership operators
list=[1,2,3,4,5]
print(1 in list) #check if 1 is in list
print(6 not in list) #check if 6 is not in list
# bitwise operators
print(a&b) #bitwise and
print(a|b) #bitwise or
print(a^b) #bitwise xor
print(~a) #bitwise not
print(a<<b) #bitwise left shift
print(a>>b) #bitwise right shift
